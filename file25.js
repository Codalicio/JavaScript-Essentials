// while loop :

let h = 18;

while (h <= 10) {
    console.log(h);
    h++;
}

console.log(`Current value of h is ${h}.`);

// Or,

let i = 0;

while (i <= 10) {
    console.log(i);
    i++;
}

console.log(`Current value of i is ${i}.`);


// do while loop :

let j = 0;

do {
    console.log(j);
    j++;
} while (j <= 14);

console.log(`Current value of j is ${j}.`);

// Or,

let days = ['Sunday', 'Monday', 'Tuesday', 'Wednesday', 'Thursday', 'Friday', 'Saturday'];
let k = 0;

do{
    console.log(days[k]);
    k++;
} while (k <= days.length - 1);




