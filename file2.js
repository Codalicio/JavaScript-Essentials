"use strict";
// Intro to variables :

// Variables can store some information
// that we can use later and we can also
// change that information later!

// Declaring a variable :

// Here 'var' is a special keyword, used for declaring a variable.

var firstName = `Amit`;

// Collection of characters is called a 'string',
// like "Amit", 'Amit' or `Amit`.


// Using a variable :

// Variable name are 'sensitive', i.e.,
// they must have same case and spelling at every
// place they are being used.

console.log(`My name is ${firstName}.`);

// Changing the variable's value :

console.log(firstName);

firstName = `Sonu`;

console.log(firstName);

