// let Keyword :

// Declaring a variable using 'let' keyword :

// We cannot use 'let' twice for declaring the same variable,
// whereas we can use var in this case but can change the value of the variable
// once declared in either of the cases!

// 'let' keyword was introduced in ES2015.

let firstName = "Amit";
console.log(firstName);

firstName = "Vishwajeet";
console.log(firstName);


